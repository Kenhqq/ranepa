#include <iostream>

using namespace std;

int main() {
    // Создаем несколько переменных
    int a = 10;
    int b = 20;
    int c = 30;

    // Выводим адреса переменных
    cout << "Адрес a: " << &a << ", значение: " << a << endl;
    cout << "Адрес b: " << &b << ", значение: " << b << endl;
    cout << "Адрес c: " << &c << ", значение: " << c << endl;

    // Указатель на переменную b
    int* ptrB = &b;

    // Неправильное использование указателя ptrB для изменения значения переменной c
    // Изменяем значение, используя арифметику указателей
    *ptrB = c; // Попытка присвоить значение переменной c переменной b

    // Выводим значения переменных после изменения
    cout << "После изменения:" << endl;
    cout << "b: " << b << endl; // b теперь должно быть равно значению c (30)
    cout << "c: " << c << endl; // c остается неизменным (30)

    // Можем также вывести адреса и проверить, что они все еще одинаковые
    cout << "Адрес a: " << &a << ", значение: " << a << endl;
    cout << "Адрес b: " << &b << ", значение: " << b << endl;
    cout << "Адрес c: " << &c << ", значение: " << c << endl;

    return 0;
}
